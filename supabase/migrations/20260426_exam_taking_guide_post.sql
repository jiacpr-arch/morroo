<h3>โครงสร้างคำตอบที่ผู้ตรวจอยากเห็น</h3>
<ul>
  <li><strong>เขียนเป็นข้อ ๆ ไม่ใช่ paragraph</strong> — ผู้ตรวจไล่หา key points ง่ายกว่า</li>
  <li><strong>ใส่ dose, route, frequency ให้ครบ</strong> ทุกครั้งที่สั่งยา (เช่น Ceftriaxone 2g IV OD)</li>
  <li><strong>ลำดับ DD จาก most likely → must-not-miss</strong> — แสดงว่าคิดเป็นระบบ</li>
  <li><strong>เขียนเหตุผลสั้น ๆ ต่อท้ายแต่ละ DD</strong> เช่น "Acute MI — chest pain + ST elevation in V1-V4"</li>
</ul>

<h3>เมื่อ "ติด" ในตอนใดตอนหนึ่ง</h3>
<ul>
  <li><strong>ข้ามทันที</strong> ถ้าคิดเกิน 3 นาทีแล้วยังไม่มีไอเดีย — ตอนต่อไปอาจ "เฉลย" ตอนที่ติดอยู่</li>
  <li>เขียนคำตอบที่เป็นไปได้ที่สุด (best guess) ลงไปก่อน — ปล่อยว่าง = 0 คะแนน</li>
  <li>กลับมาเติมตอนตรวจทาน</li>
</ul>

<h2>การรับมือความเครียดในห้องสอบ</h2>
<ul>
  <li><strong>หายใจเข้าลึก ๆ 4 วินาที กลั้น 4 วินาที หายใจออก 4 วินาที</strong> ทำ 3 รอบ — ลด heart rate ลงได้จริง</li>
  <li>เจอ case ที่ไม่เคยเห็น — บอกตัวเองว่า "คนอื่นก็ไม่เคยเห็นเหมือนกัน"</li>
  <li>อย่าเทียบความเร็วกับเพื่อนข้าง ๆ — บางคนเขียนเสร็จเร็วเพราะเขียนผิด</li>
</ul>

<h2>หลังสอบ</h2>
<ul>
  <li><strong>อย่ารีบเช็คเฉลยทันที</strong> — ถ้ายังต้องสอบวันที่ 2 (MEQ) จะเสีย confidence</li>
  <li>หลังสอบครบทั้ง 2 วัน — พักก่อน อย่ามานั่งคำนวณคะแนน</li>
  <li>ถ้าผ่าน = ดีมาก ถ้าไม่ผ่าน = ทำ feedback กับตัวเอง 1 หน้ากระดาษ ว่าจุดไหนต้องแก้</li>
</ul>

