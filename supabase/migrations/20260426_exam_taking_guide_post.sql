<h2>แนวทางทำข้อสอบ MCQ (200 ข้อ / 180 นาที)</h2>
<h3>กลยุทธ์ 3 รอบ</h3>
<ol>
  <li><strong>รอบที่ 1 (90 นาที):</strong> ทำเฉพาะข้อที่มั่นใจ ตอบทันที ข้อไหนสงสัยให้ทำเครื่องหมาย "?" แล้วข้าม</li>
  <li><strong>รอบที่ 2 (60 นาที):</strong> กลับมาทำข้อที่ทำเครื่องหมาย "?" คิดให้เต็มที่</li>
  <li><strong>รอบที่ 3 (30 นาที):</strong> ข้อที่ยังไม่รู้ — ใช้กลยุทธ์ educated guess (ดูด้านล่าง)</li>
</ol>

<h3>เทคนิคอ่านโจทย์ MCQ</h3>
<ul>
  <li><strong>อ่านคำถามก่อน stem</strong> — รู้ว่าโจทย์ต้องการอะไร แล้วค่อยกลับไปอ่าน case</li>
  <li><strong>ดู keyword สำคัญ:</strong> "most likely", "next step", "best initial", "definitive" — แต่ละคำต้องการคำตอบต่างกัน</li>
  <li><strong>ระวัง distractor ที่ "ถูกแต่ไม่ใช่คำตอบ"</strong> — บางตัวเลือกเป็น management ที่ทำได้ แต่ไม่ใช่ next step</li>
</ul>

<h3>Educated guess เมื่อไม่รู้คำตอบ</h3>
<ul>
  <li>ตัด choice ที่ <em>ผิดแน่นอน</em> ออกก่อน 1-2 ตัว</li>
  <li>เลือก choice ที่ "ปลอดภัยที่สุดต่อผู้ป่วย"</li>
  <li>เมื่อเลือกระหว่าง investigation ราคาแพง vs. ถูก — มักเป็น "ถูกและทำได้ก่อน"</li>
  <li>อย่าเปลี่ยนคำตอบที่เลือกครั้งแรก ถ้าไม่มีเหตุผลใหม่ที่ชัดเจน</li>
</ul>

<h2>แนวทางทำข้อสอบ MEQ (5 เคส / 150 นาที)</h2>
