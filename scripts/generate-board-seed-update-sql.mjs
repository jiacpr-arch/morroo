/**
 * Generate an UPDATE SQL file that pushes the fixed `choices` JSON and
 * `explanation` text from board-seed-data/*.json into already-inserted
 * `mcq_questions` rows (where exam_source = 'AI-generated-board-seed').
 *
 * The regular seed SQL (generate-board-seed-sql.mjs) uses a NOT EXISTS
 * guard, so re-running it does NOT touch existing rows. This script is
 * what you run AFTER fix-board-seed-choices.mjs to fix production data.
 *
 * Match key: (exam_source, board_specialty, scenario).
 */

import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const SEED_DIR = join(HERE, "board-seed-data");
const OUT_DIR = join(HERE, "..", "supabase", "board_seed_update_split");
const OUT_COMBINED = join(HERE, "..", "supabase", "board_seed_update.sql");

const SLUG_TO_PREFIX = {
  emergency_medicine: "em",
  internal_medicine: "im",
  surgery: "surg",
  pediatrics: "peds",
  ob_gyn: "obg",
  orthopedics: "ortho",
  psychiatry: "psych",
  anesthesiology: "anes",
  radiology: "rad",
  family_medicine: "fammed",
  pathology: "path",
  rehab_medicine: "rehab",
};

function sqlString(s) {
  if (s === null || s === undefined) return "NULL";
  return "'" + String(s).replace(/'/g, "''") + "'";
}

function sqlJsonb(obj) {
  if (obj === null || obj === undefined) return "NULL";
  return sqlString(JSON.stringify(obj)) + "::jsonb";
}

mkdirSync(OUT_DIR, { recursive: true });

const files = readdirSync(SEED_DIR).filter((f) => f.endsWith(".json")).sort();
const combinedLines = [];
combinedLines.push("-- ===============================================================");
combinedLines.push("-- หมอรู้ — Board seed UPDATE: rewrite bloated choices + explanation");
combinedLines.push("-- Generated by scripts/generate-board-seed-update-sql.mjs");
combinedLines.push("-- Run this AFTER fix-board-seed-choices.mjs to update existing rows.");
combinedLines.push("-- Match key: (exam_source, board_specialty, scenario).");
combinedLines.push("-- ===============================================================");
combinedLines.push("");
combinedLines.push("begin;");
combinedLines.push("");

let totalUpdates = 0;

for (const file of files) {
  const slug = file.replace(/\.json$/, "");
  if (!SLUG_TO_PREFIX[slug]) continue;
  const questions = JSON.parse(readFileSync(join(SEED_DIR, file), "utf8"));

  const lines = [];
  lines.push("-- ===============================================================");
  lines.push(`-- UPDATE: ${slug} (${questions.length} questions)`);
  lines.push("-- ===============================================================");
  lines.push("");
  lines.push("begin;");
  lines.push("");

  for (const q of questions) {
    if (!q.scenario || !Array.isArray(q.choices)) continue;
    const choicesSql = sqlJsonb(q.choices);
    const explSql = sqlString(q.explanation);
    const scenarioSql = sqlString(q.scenario);

    const stmt = [
      "update public.mcq_questions",
      `set choices = ${choicesSql},`,
      `    explanation = ${explSql}`,
      `where exam_source = 'AI-generated-board-seed'`,
      `  and board_specialty = ${sqlString(slug)}`,
      `  and scenario = ${scenarioSql};`,
      "",
    ].join("\n");

    lines.push(stmt);
    combinedLines.push(stmt);
    totalUpdates++;
  }

  lines.push("commit;");
  lines.push("");

  const numPrefix = String(Object.keys(SLUG_TO_PREFIX).indexOf(slug) + 1).padStart(2, "0");
  const outPath = join(OUT_DIR, `${numPrefix}_${slug}_update.sql`);
  writeFileSync(outPath, lines.join("\n"), "utf8");
  console.log(`Wrote ${outPath} (${questions.length} updates)`);
}

combinedLines.push("commit;");
combinedLines.push("");
writeFileSync(OUT_COMBINED, combinedLines.join("\n"), "utf8");
console.log(`\nWrote ${OUT_COMBINED}`);
console.log(`Total UPDATE statements: ${totalUpdates}`);
