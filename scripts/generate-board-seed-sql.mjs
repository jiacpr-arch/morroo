/**
 * Convert all scripts/board-seed-data/*.json into ONE self-contained
 * SQL file that can be pasted into the Supabase SQL Editor.
 *
 * The output:
 *   1. INSERTs missing mcq_subjects rows for the 11 non-EM specialties
 *      (em_* rows already inserted by supabase/board_seed.sql).
 *   2. INSERTs all questions into mcq_questions with subject_id resolved
 *      via subquery (SELECT id FROM mcq_subjects WHERE name = '...').
 *
 * Safe to re-run: uses ON CONFLICT DO NOTHING for subjects + an existence
 * guard so duplicate questions are not inserted twice.
 */

import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const SEED_DIR = join(HERE, "board-seed-data");
const OUT_FILE = join(HERE, "..", "supabase", "board_seed_questions.sql");
const OUT_DIR_SPLIT = join(HERE, "..", "supabase", "board_seed_questions_split");

const SUBJECT_PREFIX = {
  emergency_medicine: "em",
  internal_medicine: "im",
  surgery: "surg",
  pediatrics: "peds",
  ob_gyn: "obg",
  orthopedics: "ortho",
  psychiatry: "psych",
  anesthesiology: "anes",
  radiology: "rad",
  family_medicine: "fammed",
  pathology: "path",
  rehab_medicine: "rehab",
};

const SECTION_ICON = {
  clinical_decision: "🩺",
  basic_science: "🧬",
  ems_mgmt: "🚨",
  integrative: "🧩",
};

const SECTION_LABEL_TH = {
  clinical_decision: "การตัดสินใจทางเวชกรรม",
  basic_science: "วิทยาศาสตร์การแพทย์พื้นฐาน",
  ems_mgmt: "การจัดการระบบบริการการแพทย์ฉุกเฉิน",
  integrative: "ข้อสอบบูรณาการ",
};

const SPECIALTY_NAME_TH = {
  emergency_medicine: "เวชศาสตร์ฉุกเฉิน",
  internal_medicine: "อายุรศาสตร์",
  surgery: "ศัลยศาสตร์",
  pediatrics: "กุมารเวชศาสตร์",
  ob_gyn: "สูติศาสตร์-นรีเวชวิทยา",
  orthopedics: "ออร์โธปิดิกส์",
  psychiatry: "จิตเวชศาสตร์",
  anesthesiology: "วิสัญญีวิทยา",
  radiology: "รังสีวิทยา",
  family_medicine: "เวชศาสตร์ครอบครัว",
  pathology: "พยาธิวิทยา",
  rehab_medicine: "เวชศาสตร์ฟื้นฟู",
};

// SQL string literal. With standard_conforming_strings = on (Postgres default
// since 9.1), backslashes are NOT escape characters inside ordinary string
// literals, so we must only double single quotes. Doubling backslashes would
// corrupt embedded JSON escapes like \"awaken\".
function sqlString(s) {
  if (s === null || s === undefined) return "NULL";
  return "'" + String(s).replace(/'/g, "''") + "'";
}

function sqlBool(b) {
  return b ? "true" : "false";
}

function sqlJsonb(obj) {
  if (obj === null || obj === undefined) return "NULL";
  const json = JSON.stringify(obj);
  return sqlString(json) + "::jsonb";
}

function difficultyOrDefault(d) {
  return ["easy", "medium", "hard"].includes(d) ? d : "medium";
}

// ──────────────────────────────────────────────────────────────────────────
// Build SQL
// ──────────────────────────────────────────────────────────────────────────

const lines = [];
lines.push("-- ===============================================================");
lines.push("-- หมอรู้ — Board seed: 360 MCQs across 12 specialties");
lines.push("-- Generated by scripts/generate-board-seed-sql.mjs");
lines.push("-- Safe to paste into Supabase SQL Editor. Re-runnable.");
lines.push("-- ===============================================================");
lines.push("");
lines.push("begin;");
lines.push("");

// 1. Ensure mcq_subjects rows exist for all 12 specialties × 4 sections.
lines.push("-- 1/2 ─── mcq_subjects (auto-create missing rows) ────────────────");
lines.push("insert into public.mcq_subjects");
lines.push("  (name, name_th, icon, audience, board_specialty, exam_type, question_count)");
lines.push("values");
const subjRows = [];
for (const [slug, prefix] of Object.entries(SUBJECT_PREFIX)) {
  const nameTh = SPECIALTY_NAME_TH[slug];
  for (const section of Object.keys(SECTION_LABEL_TH)) {
    subjRows.push(
      `  (${sqlString(`${prefix}_${section}`)}, ` +
        `${sqlString(`${nameTh} · ${SECTION_LABEL_TH[section]}`)}, ` +
        `${sqlString(SECTION_ICON[section])}, ` +
        `'board', ${sqlString(slug)}, NULL, 0)`
    );
  }
}
lines.push(subjRows.join(",\n"));
lines.push("on conflict (name) do nothing;");
lines.push("");

// 2. INSERT all 360 questions.
lines.push("-- 2/2 ─── mcq_questions (360 board questions) ────────────────────");
lines.push("-- Each row resolves subject_id via subquery on mcq_subjects.name.");
lines.push("-- exam_source tag makes the seed easy to find/delete later.");
lines.push("");

const files = readdirSync(SEED_DIR).filter((f) => f.endsWith(".json")).sort();
let totalQ = 0;
let totalInvalid = 0;

for (const file of files) {
  const slug = file.replace(/\.json$/, "");
  const prefix = SUBJECT_PREFIX[slug];
  if (!prefix) {
    console.warn(`Skipping unknown specialty: ${slug}`);
    continue;
  }
  const questions = JSON.parse(readFileSync(join(SEED_DIR, file), "utf8"));
  lines.push(`-- ── ${slug} (${questions.length} questions) ─────────────────────`);

  for (const q of questions) {
    // Sanity check.
    if (
      !q.scenario ||
      !Array.isArray(q.choices) ||
      q.choices.length !== 5 ||
      !["A", "B", "C", "D", "E"].includes(q.correct_answer)
    ) {
      totalInvalid++;
      lines.push(`-- INVALID question skipped`);
      continue;
    }
    totalQ++;

    const subjectName = `${prefix}_${q.board_section}`;
    lines.push("insert into public.mcq_questions (");
    lines.push("  subject_id, audience, exam_type, scenario, choices, correct_answer,");
    lines.push("  explanation, detailed_explanation, difficulty, topic, status,");
    lines.push("  board_specialty, board_section, board_topic, board_age_group,");
    lines.push("  reference_source, exam_source, is_ai_enhanced, ai_notes");
    lines.push(")");
    lines.push("select");
    lines.push(`  s.id, 'board', NULL, ${sqlString(q.scenario)}, ${sqlJsonb(q.choices)},`);
    lines.push(
      `  ${sqlString(q.correct_answer)}, ${sqlString(q.explanation)}, ${sqlJsonb(q.detailed_explanation)},`
    );
    lines.push(
      `  ${sqlString(difficultyOrDefault(q.difficulty))}, ${sqlString(q.board_topic || null)}, 'review',`
    );
    lines.push(
      `  ${sqlString(slug)}, ${sqlString(q.board_section)}, ${sqlString(q.board_topic || "general")}, ${sqlString(q.board_age_group)},`
    );
    lines.push(
      `  ${sqlString(q.reference_source)}, 'AI-generated-board-seed', true, 'seeded via Claude Code session (no critique pass)'`
    );
    lines.push(`from public.mcq_subjects s`);
    lines.push(`where s.name = ${sqlString(subjectName)}`);
    lines.push("  and s.audience = 'board'");
    lines.push("  and not exists (");
    lines.push("    select 1 from public.mcq_questions q");
    lines.push(
      `    where q.exam_source = 'AI-generated-board-seed'`
    );
    lines.push(`      and q.board_specialty = ${sqlString(slug)}`);
    lines.push(`      and q.scenario = ${sqlString(q.scenario)}`);
    lines.push("  );");
    lines.push("");
  }
}

lines.push("");
lines.push("-- ─── verify ─────────────────────────────────────────────────────");
lines.push("select board_specialty, board_section, count(*) as inserted");
lines.push("from public.mcq_questions");
lines.push("where exam_source = 'AI-generated-board-seed'");
lines.push("group by 1, 2");
lines.push("order by 1, 2;");
lines.push("");
lines.push("commit;");

writeFileSync(OUT_FILE, lines.join("\n") + "\n", "utf8");
console.log(`Wrote ${OUT_FILE}`);
console.log(`Total questions: ${totalQ}`);
console.log(`Invalid skipped: ${totalInvalid}`);
console.log(`Lines: ${lines.length}`);

// ──────────────────────────────────────────────────────────────────────────
// Also write per-specialty SQL files for users who hit the Supabase SQL
// Editor query-size limit on the combined file.
// ──────────────────────────────────────────────────────────────────────────

mkdirSync(OUT_DIR_SPLIT, { recursive: true });

for (const file of files) {
  const slug = file.replace(/\.json$/, "");
  const prefix = SUBJECT_PREFIX[slug];
  if (!prefix) continue;
  const questions = JSON.parse(readFileSync(join(SEED_DIR, file), "utf8"));
  const nameTh = SPECIALTY_NAME_TH[slug];

  const splitLines = [];
  splitLines.push("-- ===============================================================");
  splitLines.push(`-- หมอรู้ — Board seed: ${nameTh} (${slug}) — ${questions.length} MCQs`);
  splitLines.push("-- Safe to paste into Supabase SQL Editor. Re-runnable.");
  splitLines.push("-- ===============================================================");
  splitLines.push("");
  splitLines.push("begin;");
  splitLines.push("");

  splitLines.push("-- 1/2 ─── mcq_subjects for this specialty ────────────────────");
  splitLines.push("insert into public.mcq_subjects");
  splitLines.push("  (name, name_th, icon, audience, board_specialty, exam_type, question_count)");
  splitLines.push("values");
  const subjRowsThis = [];
  for (const section of Object.keys(SECTION_LABEL_TH)) {
    subjRowsThis.push(
      `  (${sqlString(`${prefix}_${section}`)}, ` +
        `${sqlString(`${nameTh} · ${SECTION_LABEL_TH[section]}`)}, ` +
        `${sqlString(SECTION_ICON[section])}, ` +
        `'board', ${sqlString(slug)}, NULL, 0)`
    );
  }
  splitLines.push(subjRowsThis.join(",\n"));
  splitLines.push("on conflict (name) do nothing;");
  splitLines.push("");

  splitLines.push(`-- 2/2 ─── ${questions.length} mcq_questions for ${slug} ─────────────`);
  splitLines.push("");

  for (const q of questions) {
    if (
      !q.scenario ||
      !Array.isArray(q.choices) ||
      q.choices.length !== 5 ||
      !["A", "B", "C", "D", "E"].includes(q.correct_answer)
    ) {
      splitLines.push("-- INVALID question skipped");
      continue;
    }
    const subjectName = `${prefix}_${q.board_section}`;
    splitLines.push("insert into public.mcq_questions (");
    splitLines.push("  subject_id, audience, exam_type, scenario, choices, correct_answer,");
    splitLines.push("  explanation, detailed_explanation, difficulty, topic, status,");
    splitLines.push("  board_specialty, board_section, board_topic, board_age_group,");
    splitLines.push("  reference_source, exam_source, is_ai_enhanced, ai_notes");
    splitLines.push(")");
    splitLines.push("select");
    splitLines.push(
      `  s.id, 'board', NULL, ${sqlString(q.scenario)}, ${sqlJsonb(q.choices)},`
    );
    splitLines.push(
      `  ${sqlString(q.correct_answer)}, ${sqlString(q.explanation)}, ${sqlJsonb(q.detailed_explanation)},`
    );
    splitLines.push(
      `  ${sqlString(difficultyOrDefault(q.difficulty))}, ${sqlString(q.board_topic || null)}, 'review',`
    );
    splitLines.push(
      `  ${sqlString(slug)}, ${sqlString(q.board_section)}, ${sqlString(q.board_topic || "general")}, ${sqlString(q.board_age_group)},`
    );
    splitLines.push(
      `  ${sqlString(q.reference_source)}, 'AI-generated-board-seed', true, 'seeded via Claude Code session (no critique pass)'`
    );
    splitLines.push(`from public.mcq_subjects s`);
    splitLines.push(`where s.name = ${sqlString(subjectName)}`);
    splitLines.push("  and s.audience = 'board'");
    splitLines.push("  and not exists (");
    splitLines.push("    select 1 from public.mcq_questions q");
    splitLines.push("    where q.exam_source = 'AI-generated-board-seed'");
    splitLines.push(`      and q.board_specialty = ${sqlString(slug)}`);
    splitLines.push(`      and q.scenario = ${sqlString(q.scenario)}`);
    splitLines.push("  );");
    splitLines.push("");
  }

  splitLines.push("commit;");
  splitLines.push("");
  splitLines.push("-- verify");
  splitLines.push("select board_section, count(*) from public.mcq_questions");
  splitLines.push(`where board_specialty = ${sqlString(slug)} and exam_source = 'AI-generated-board-seed'`);
  splitLines.push("group by 1 order by 1;");

  const outName = join(OUT_DIR_SPLIT, `${String(files.indexOf(file) + 1).padStart(2, "0")}_${slug}.sql`);
  writeFileSync(outName, splitLines.join("\n") + "\n", "utf8");
  console.log(`Wrote ${outName} (${questions.length} questions)`);
}
